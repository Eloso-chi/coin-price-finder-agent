/**
 * Look up a PCGS coin number from series, year, and mint.
 * @param {string} series — parsed series name (e.g. "Lincoln", "Morgan")
 * @param {number} year — coin year
 * @param {string} [mint] — mint mark (P, D, S, O, CC, W)
 * @returns {number|null} PCGS coin number, or null if not found
 */
function lookupPCGSNumber(series, year, mint) {
  if (!series || !year) return null;

  const seriesStr = String(series).trim();
  const m = (mint || '').toUpperCase() || 'P';

  for (const entry of SERIES_MAP) {
    if (!entry.re.test(seriesStr)) continue;

    // Try single table or array of tables
    const tables = entry.tables || [entry.table];
    for (const tbl of tables) {
      const yearData = tbl[year];
      if (!yearData) continue;
      // Try exact mint, fall back to P (Philadelphia = no mint mark)
      return yearData[m] || yearData['P'] || Object.values(yearData)[0] || null;
    }
  }

  return null;
}

module.exports = { lookupPCGSNumber };
