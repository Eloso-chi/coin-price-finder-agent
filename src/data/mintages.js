/**
 * Normalize a series name to match MINTAGE_DATA keys.
 */
function normalizeSeries(series) {
  if (!series) return null;
  const s = series.toLowerCase().trim()
    .replace(/\s+/g, ' ')
    .replace(/half\s*dollar/i, 'half dollar')
    .replace(/quarter\s*dollar/i, 'quarter');

  if (MINTAGE_DATA[s]) return s;
  if (SERIES_ALIASES[s]) return SERIES_ALIASES[s];

  // Fuzzy: check if any alias is a substring
  for (const [alias, canonical] of Object.entries(SERIES_ALIASES)) {
    if (s.includes(alias)) return canonical;
  }
  // Check if any canonical key is a substring
  for (const key of Object.keys(MINTAGE_DATA)) {
    if (s.includes(key) || key.includes(s)) return key;
  }
  return null;
}

/**
 * Look up mintage for a coin.
 * @param {string} series  e.g. "Morgan Dollar", "ASE", "Mercury Dime"
 * @param {number} year    e.g. 1893
 * @param {string} [mint]  e.g. "S", "CC", "D", "P", "O", "W"
 * @param {number} [weight] e.g. 0.5, 0.25, 0.1 (oz) for fractional bullion
 * @returns {{ mintage: number|null, series: string|null }}
 */
function lookupMintage(series, year, mint, weight) {
  const canonical = normalizeSeries(series);
  if (!canonical || !year) return { mintage: null, series: canonical };

  const m = (mint || 'P').toUpperCase().trim();
  const key = `${year}-${m}`;
  const pKey = `${year}-P`;

  // ── Try weight-specific fractional table first ──
  if (weight && weight !== 1) {
    const fractionMap = { 0.5: '1/2 oz', 0.25: '1/4 oz', 0.1: '1/10 oz', 0.05: '1/20 oz', 1.5: '1.5 oz', 2: '2 oz', 5: '5 oz', 10: '10 oz' };
    const suffix = fractionMap[weight];
    if (suffix) {
      const fractionalKey = `${canonical} ${suffix}`;
      const fracTable = MINTAGE_DATA[fractionalKey];
      if (fracTable) {
        if (fracTable[key] != null) return { mintage: fracTable[key], series: fractionalKey };
        if (!mint && fracTable[pKey] != null) return { mintage: fracTable[pKey], series: fractionalKey };
      }
    }
  }

  // ── Fall back to standard (1 oz) table ──
  const table = MINTAGE_DATA[canonical];
  if (!table) return { mintage: null, series: canonical };

  if (table[key] != null) {
    return { mintage: table[key], series: canonical };
  }

  // If no mint specified, try P
  if (!mint) {
    if (table[pKey] != null) return { mintage: table[pKey], series: canonical };
  }

  return { mintage: null, series: canonical };
}

